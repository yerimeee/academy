package variable02;

public class variable03 {

	public static void main(String[] args) {

		byte by1 = 10;
		System.out.println(by1);
		
		short sh = 30000;
		System.out.println(sh);
		
		char ch1 = '가';
		int chInt = ch1; //문자를 정수형으로 바꾸는 것. = 형변환
		System.out.println("'가'의 유니코드 : " + chInt);

		double dou1 = 5/3.0;
		System.out.println(dou1);
		
		
	}

}
