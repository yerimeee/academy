package operator03;

public class Operator05 {

	public static void main(String[] args) {

		//복합 대입 연산자
		int num1 = 10;
		num1 += 10; // num1 = num1 + 10;
		num1 *= 3; // num1 = num1 * 3;
		num1 -= 5; // num1 = num1 - 5;
		num1 /= 3; // num1 = num1 / 3;
		num1 %= 5; // num1 = num1 % 5;
		System.out.println(num1);
	}
}
